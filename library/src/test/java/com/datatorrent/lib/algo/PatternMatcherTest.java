package com.datatorrent.lib.algo;

/**
 *
 * Functional tests for {@link com.datatorrent.lib.algo.AbstractPatternMatcher}<p>
 *
 */

import java.util.ArrayList;
import java.util.List;
import java.util.Random;

import junit.framework.Assert;
import org.junit.After;
import org.junit.Before;
import org.junit.Test;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import com.google.common.collect.Lists;

import com.datatorrent.api.DefaultOutputPort;

import com.datatorrent.lib.testbench.CollectorTestSink;

/**
 *
 */
public class PatternMatcherTest
{

  public static class PatternMatcher<T> extends AbstractPatternMatcher<T>
  {
    @Override
    public void processPatternFound(List<T> outputList)
    {
      outputPort.emit(new ArrayList<T>(outputList));
    }

    public transient DefaultOutputPort<List<T>> outputPort = new DefaultOutputPort<List<T>>();
  }

  private PatternMatcher<Integer> patternMatcher;
  private AbstractPatternMatcher.Pattern<Integer> pattern;
  private List<Integer> inputPattern;
  private CollectorTestSink sink;

  @Before
  public void setup()
  {
    inputPattern = Lists.newArrayList();
    pattern = new AbstractPatternMatcher.Pattern<Integer>();
    patternMatcher = new PatternMatcher<Integer>();
    sink = new CollectorTestSink();
    patternMatcher.outputPort.setSink(sink);
  }

  @After
  public void cleanup()
  {
    patternMatcher.teardown();
    inputPattern.clear();
    sink.collectedTuples.clear();

  }

  @Test
  public void test() throws Exception
  {
    inputPattern.add(0);
    inputPattern.add(1);
    inputPattern.add(0);
    inputPattern.add(1);
    inputPattern.add(2);
    pattern.setPattern(inputPattern);
    patternMatcher.setPattern(pattern);
    patternMatcher.setup(null);
    patternMatcher.beginWindow(0);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(1);
    patternMatcher.inputPort.process(1);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(1);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(1);
    patternMatcher.inputPort.process(2);
    patternMatcher.inputPort.process(1);
    patternMatcher.endWindow();
    Assert.assertEquals("The number of tuples emited is one", 1, sink.collectedTuples.size());
    List<Integer> outputList = sink.collectedTuples;
    for (int i = 0; i < outputList.size(); i++) {
      Assert.assertEquals("Matching the output pattern with input pattern", inputPattern, outputList.get(i));
    }
  }

  @Test
  public void testSimplePattern() throws Exception
  {
    inputPattern.add(0);
    inputPattern.add(0);
    pattern.setPattern(inputPattern);
    patternMatcher.setPattern(pattern);
    patternMatcher.setup(null);
    patternMatcher.beginWindow(0);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(1);
    patternMatcher.inputPort.process(0);
    patternMatcher.inputPort.process(0);
    patternMatcher.endWindow();
    Assert.assertEquals("The number of tuples emited are three", 3, sink.collectedTuples.size());
    List<Integer> outputList = sink.collectedTuples;
    for (int i = 0; i < outputList.size(); i++) {
      Assert.assertEquals("Matching the output pattern with input pattern", inputPattern, outputList.get(i));
    }
  }

  @Test
  public void testAutoGeneratedPattern() throws Exception
  {
    Random random = new Random();
    int patternSize = 15;
    int max = 10;
    int min = 1;
    int primeNumber = 5;
    for (int i = 0; i < patternSize; i++) {
      inputPattern.add(min + random.nextInt(max));
    }
    pattern.setPattern(inputPattern);
    patternMatcher.setPattern(pattern);
    patternMatcher.setup(null);
    patternMatcher.beginWindow(0);
    int numberOfIterations = 20;
    for (int i = 0; i < patternSize; i++) {
      for (int j = 0; j <= i; j++) {
        patternMatcher.inputPort.process(inputPattern.get(j));
      }
      for (int k = 0; k < numberOfIterations; k++) {
        patternMatcher.inputPort.process(max + min + random.nextInt(max));
      }
      if (i % primeNumber == 0) {
        for (int j = 0; j < patternSize; j++) {
          patternMatcher.inputPort.process(inputPattern.get(j));
        }
      }
    }
    patternMatcher.endWindow();
    Assert.assertEquals("The number of tuples emited ", 1 + patternSize / primeNumber, sink.collectedTuples.size());
    List<Integer> outputList = sink.collectedTuples;
    for (int i = 0; i < outputList.size(); i++) {
      Assert.assertEquals("Matching the output pattern with input pattern", inputPattern, outputList.get(i));
    }
  }

  private static final Logger logger = LoggerFactory.getLogger(PatternMatcherTest.class);
}
